function getRandomWord() {
  return englishWords[Math.floor(Math.random() * englishWords.length)];
}

export default function generateRandomUsername() {
  const randomWord1 = getRandomWord();
  const includeSecondWord = Math.random() < 0.5; // 50% chance to include a second word
  const randomWord2 = includeSecondWord ? getRandomWord() : "";
  const randomNumbers = Math.floor(Math.random() * 1000); // Generate up to 3-digit random number
  const separator = "_";

  let username = randomWord1;

  if (randomWord2) {
    username += separator + randomWord2;
  }

  username += separator + randomNumbers;

  return username;
}
